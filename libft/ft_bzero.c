#include <stddef.h>

void	ft_bzero(void *str, size_t i)
{
	size_t			j;
	unsigned char	*ptr;

	ptr = (unsigned char *)str;
	j = 0;
	while (j < i)
	{
		ptr[j] = '\0';
		j++;
	}
}
